/**
 * Render `AssertionPlan`s to TypeScript using the harness-first
 * `describeEval(name, { harness, judgeThreshold }, (raw) => {...})`
 * callback form on top of upstream `vitest-evals`.
 *
 * Two render entrypoints:
 *
 * - `renderEvalFile(entryId, consolidated, sharedJudges)` — emits
 *   one `.eval.ts` per spec entry. Imports the named judges it
 *   references from `./_judges.js`. When a case has a fixture tree
 *   on disk, the test calls
 *   `createWorkspace(skillRoot, "<slug>")` and passes the
 *   resulting cwd to `run()` via `metadata`.
 * - `renderJudgesFile(judges)` — emits the suite-wide
 *   `_judges.ts` containing the canonical set of judges, one
 *   `export const FooJudge = criterionJudge("FooJudge", "...")`
 *   per entry.
 *
 * Plus `validatePlan(entryId, plan)` — runs the renderer's
 * contract checks against an `AssertionPlan` *before*
 * consolidation. Used by the eval-gen generate loop so contract
 * violations bubble back as parse-equivalent retry signals; the
 * consolidate pass and final render don't have to re-validate.
 *
 * Pure functions: same input → same string output, no I/O.
 */

import type {
  Assertion,
  AssertionPlan,
  ConsolidatedCasePlan,
  ConsolidatedPlan,
  JudgeAssertion,
  JudgePlan,
  OutputMatchObjectAssertion,
  ToolCallExpectation,
  ToolCallsAssertion,
} from "./eval-gen-types.js";

/** Banner placed at the top of generated eval files. */
export const EVAL_TS_BANNER = `// ──────────────────────────────────────────────────────────
// Generated initially from spec.yaml; durable after that. Edit
// freely to refine prompts, setup, and assertions for this
// behavior. Add or remove behaviors via spec.yaml — skillet only
// regenerates eval files for behaviors that don't have one yet.
// ──────────────────────────────────────────────────────────
`;

/** Banner for the suite-wide `_judges.ts` file. */
export const JUDGES_TS_BANNER = `// ──────────────────────────────────────────────────────────
// Generated by skillet eval-gen consolidation. Do NOT edit
// directly — judges are deduped from per-behavior plans on
// every regen and this file is rewritten as a whole.
// To tune a judge's criterion, edit the relevant behavior's
// plan (or use \`skillet improve\`); criteria flow into this
// file through the consolidation pass.
// ──────────────────────────────────────────────────────────
`;

/**
 * Thrown when a plan or its rendered output violates a renderer
 * guardrail. eval-gen catches it and uses the message as the
 * parse-equivalent retry signal so the LLM can fix the plan.
 */
export class RenderError extends Error {
  constructor(message: string) {
    super(message);
    this.name = "RenderError";
  }
}

export interface RenderOptions {
  /** Indent string for nested blocks. Default `"  "` (two spaces). */
  indent?: string;
}

const DEFAULT_INDENT = "  ";

/** Pattern for a JS identifier (used to validate judge names). */
const JUDGE_NAME_RE = /^[A-Z][A-Za-z0-9]*Judge$/;

/**
 * Maximum chars in a judge `criterion`. The generator targets 200
 * (per the code-eval contract); the renderer's 300 absorbs minor
 * overruns the verifier didn't catch without blocking output.
 */
const MAX_CRITERION_CHARS = 300;

/** Per-entry judge cap (cross-suite consolidation can have many more). */
const MAX_JUDGES_PER_FILE = 5;

// ── Validation (pre-consolidation) ─────────────────────────────────────────

/**
 * Validate an `AssertionPlan` against the renderer's contract caps.
 * Runs at generate time so contract violations surface as retry
 * signals before the plan reaches consolidation.
 *
 * Throws `RenderError` with a specific, actionable message on the
 * first violation.
 */
export const validatePlan = (entryId: string, plan: AssertionPlan): void => {
  if (!Array.isArray(plan.cases) || plan.cases.length === 0) {
    throw new RenderError("plan.cases must be a non-empty array");
  }
  if (!Array.isArray(plan.judges)) {
    throw new RenderError("plan.judges must be an array (use [] for no judges)");
  }
  if (plan.judges.length > MAX_JUDGES_PER_FILE) {
    throw new RenderError(
      `plan declares ${plan.judges.length} judges (${plan.judges
        .map((j) => j.name)
        .join(
          ", ",
        )}); the code-eval contract allows at most ${MAX_JUDGES_PER_FILE} judges per file. Consolidate or drop the least-load-bearing.`,
    );
  }
  for (const judge of plan.judges) {
    validateJudgeShape(judge);
  }
  const judgeNames = new Set(plan.judges.map((j) => j.name));
  const referencedJudges = new Set<string>();
  const seenCaseNames = new Set<string>();
  for (const c of plan.cases) {
    if (typeof c.name !== "string" || c.name === "") {
      throw new RenderError("case missing 'name'");
    }
    if (seenCaseNames.has(c.name)) {
      throw new RenderError(`duplicate case name: "${c.name}"`);
    }
    seenCaseNames.add(c.name);
    if (c.tests_behavior !== entryId) {
      throw new RenderError(
        `case "${c.name}": tests_behavior "${c.tests_behavior}" does not match entry id "${entryId}"`,
      );
    }
    if (typeof c.input !== "string" || c.input === "") {
      throw new RenderError(`case "${c.name}": input must be a non-empty string`);
    }
    if (!Array.isArray(c.assertions) || c.assertions.length === 0) {
      throw new RenderError(`case "${c.name}": assertions must be a non-empty array`);
    }
    for (const a of c.assertions) {
      validateAssertion(c.name, a, judgeNames);
      if (a.kind === "judge") {
        referencedJudges.add(a.judgeName);
      }
    }
  }
  for (const j of plan.judges) {
    if (!referencedJudges.has(j.name)) {
      throw new RenderError(
        `judge "${j.name}" is declared but never referenced. Remove it from plan.judges or wire it into a case's assertions.`,
      );
    }
  }
};

const validateJudgeShape = (judge: JudgePlan): void => {
  if (!JUDGE_NAME_RE.test(judge.name)) {
    throw new RenderError(
      `judge name "${judge.name}" must be PascalCase ending in "Judge" (e.g. PwnRequestJudge)`,
    );
  }
  if (typeof judge.criterion !== "string" || judge.criterion.trim() === "") {
    throw new RenderError(`judge "${judge.name}": criterion must be a non-empty string`);
  }
  if (judge.criterion.length > MAX_CRITERION_CHARS) {
    throw new RenderError(
      `judge "${judge.name}": criterion is ${judge.criterion.length} chars (cap is ${MAX_CRITERION_CHARS}). Tighten the rubric to 1-2 sentences.`,
    );
  }
};

const validateAssertion = (caseName: string, a: Assertion, judgeNames: Set<string>): void => {
  switch (a.kind) {
    case "output-match-object": {
      if (a.value == null || typeof a.value !== "object") {
        throw new RenderError(`case "${caseName}": output-match-object value must be an object`);
      }
      return;
    }
    case "tool-calls": {
      validateToolCallsAssertion(caseName, a);
      return;
    }
    case "judge": {
      if (!judgeNames.has(a.judgeName)) {
        throw new RenderError(
          `case "${caseName}": judge assertion references unknown judge "${a.judgeName}"; declare it in plan.judges`,
        );
      }
      return;
    }
    default: {
      const exhaustive: never = a;
      throw new RenderError(`unknown assertion kind: ${JSON.stringify(exhaustive)}`);
    }
  }
};

const validateToolCallsAssertion = (caseName: string, a: ToolCallsAssertion): void => {
  const exp = a.expected;
  if (!Array.isArray(exp.names) || exp.names.length === 0) {
    throw new RenderError(
      `case "${caseName}": tool-calls.expected.names must be a non-empty array`,
    );
  }
  for (const n of exp.names) {
    if (typeof n !== "string" || n === "") {
      throw new RenderError(
        `case "${caseName}": tool-calls.expected.names entries must be non-empty strings`,
      );
    }
  }
};

// ── renderEvalFile (per-entry, post-consolidation) ─────────────────────────

/**
 * Render a per-entry `.eval.ts` from a `ConsolidatedPlan` plus the
 * suite-wide canonical judges. Imports the judges this entry's
 * cases reference from `./_judges.js`; emits
 * `await harness.useFixture(<slug>)` when a case has a fixture
 * tree on disk.
 */
export const renderEvalFile = (
  entryId: string,
  plan: ConsolidatedPlan,
  sharedJudges: JudgePlan[],
  options: RenderOptions = {},
): string => {
  const indent = options.indent ?? DEFAULT_INDENT;
  validateConsolidatedPlan(entryId, plan, sharedJudges);

  const referenced = collectReferencedJudges(plan);
  const importLines = buildEntryImports(plan, referenced);
  const describeEvalBlock = renderDescribeEval(entryId, plan.cases, indent);

  return `${EVAL_TS_BANNER}${importLines}

const skillRoot = dirname(fileURLToPath(import.meta.url)).replace(/\\/evals$/, "");

${describeEvalBlock}
`;
};

const validateConsolidatedPlan = (
  entryId: string,
  plan: ConsolidatedPlan,
  sharedJudges: JudgePlan[],
): void => {
  if (!Array.isArray(plan.cases) || plan.cases.length === 0) {
    throw new RenderError("consolidated plan.cases must be a non-empty array");
  }
  const judgeNames = new Set(sharedJudges.map((j) => j.name));
  const seenCaseNames = new Set<string>();
  for (const c of plan.cases) {
    if (typeof c.name !== "string" || c.name === "") {
      throw new RenderError("case missing 'name'");
    }
    if (seenCaseNames.has(c.name)) {
      throw new RenderError(`duplicate case name: "${c.name}"`);
    }
    seenCaseNames.add(c.name);
    if (c.tests_behavior !== entryId) {
      throw new RenderError(
        `case "${c.name}": tests_behavior "${c.tests_behavior}" does not match entry id "${entryId}"`,
      );
    }
    if (typeof c.input !== "string" || c.input === "") {
      throw new RenderError(`case "${c.name}": input must be a non-empty string`);
    }
    if (!Array.isArray(c.assertions) || c.assertions.length === 0) {
      throw new RenderError(`case "${c.name}": assertions must be a non-empty array`);
    }
    for (const a of c.assertions) {
      validateAssertion(c.name, a, judgeNames);
    }
  }
};

const collectReferencedJudges = (plan: ConsolidatedPlan): string[] => {
  const names = new Set<string>();
  for (const c of plan.cases) {
    for (const a of c.assertions) {
      if (a.kind === "judge") names.add(a.judgeName);
    }
  }
  // oxlint-disable-next-line unicorn/no-array-sort
  return [...names].sort();
};

const buildEntryImports = (plan: ConsolidatedPlan, referencedJudges: string[]): string => {
  const usesToolCalls = plan.cases.some((c) => c.assertions.some((a) => a.kind === "tool-calls"));
  const usesWorkspace = plan.cases.some((c) => c.fixtureSlug != null && c.fixtureSlug !== "");

  const skilletNamed: string[] = ["describeEval", "piAiHarness", "skilletAgent", "skilletTools"];
  if (usesToolCalls) skilletNamed.push("toolCalls");
  if (usesWorkspace) skilletNamed.push("createWorkspace");
  skilletNamed.sort();

  const lines: string[] = [
    `import { fileURLToPath } from "node:url";`,
    `import { dirname } from "node:path";`,
    `import { expect } from "vitest";`,
    `import {`,
    ...skilletNamed.map((n) => `  ${n},`),
    `} from "@sentry/skillet/evals";`,
  ];
  if (referencedJudges.length > 0) {
    lines.push(`import {`, ...referencedJudges.map((n) => `  ${n},`), `} from "./_judges.js";`);
  }
  return lines.join("\n");
};

const renderDescribeEval = (
  entryId: string,
  cases: ConsolidatedCasePlan[],
  indent: string,
): string => {
  const inner = indent + indent;
  const lines: string[] = [];
  lines.push(`describeEval(`);
  lines.push(`${indent}${JSON.stringify(entryId)},`);
  lines.push(`${indent}{`);
  lines.push(`${inner}harness: piAiHarness({`);
  lines.push(`${inner}${indent}createAgent: () => skilletAgent({ skillRoot }),`);
  lines.push(`${inner}${indent}tools: skilletTools({ skillRoot }),`);
  lines.push(`${inner}}),`);
  lines.push(`${inner}judgeThreshold: 0.75,`);
  lines.push(`${indent}},`);
  lines.push(`${indent}(it) => {`);
  cases.forEach((c, i) => {
    if (i > 0) lines.push("");
    lines.push(...renderCase(c, indent + indent));
  });
  lines.push(`${indent}},`);
  lines.push(`);`);
  return lines.join("\n");
};

const renderCase = (c: ConsolidatedCasePlan, indent: string): string[] => {
  const inner = indent + "  ";
  const body = inner + "  ";

  const usesFixture = c.fixtureSlug != null && c.fixtureSlug !== "";

  // LLM agents typically take 10-60s per case. Default to 120s
  // so we don't hit vitest's 5s default; the LLM can override
  // upward via plan.timeout when a case needs more.
  const timeout = c.timeout ?? 120_000;
  const header: string[] = [];
  header.push(`${indent}it(`);
  header.push(`${inner}${JSON.stringify(c.name)},`);
  header.push(`${inner}{ timeout: ${formatNumber(timeout)} },`);
  header.push(`${inner}async ({ run }) => {`);

  const block: string[] = [];
  if (usesFixture) {
    block.push(`${body}const cwd = createWorkspace(skillRoot, ${JSON.stringify(c.fixtureSlug)});`);
    block.push(
      `${body}const result = await run(${JSON.stringify(c.input)}, { metadata: { cwd } });`,
    );
  } else {
    block.push(`${body}const result = await run(${JSON.stringify(c.input)});`);
  }
  block.push("");

  const usesToolCalls = c.assertions.some((a) => a.kind === "tool-calls");
  if (usesToolCalls) {
    block.push(`${body}const toolNames = toolCalls(result.session).map((c) => c.name);`);
  }

  for (const a of c.assertions) {
    block.push(...renderAssertion(a, body));
  }

  return [...header, ...block, `${inner}},`, `${indent});`];
};

const renderAssertion = (a: Assertion, indent: string): string[] => {
  switch (a.kind) {
    case "output-match-object":
      return [renderOutputMatchObject(a, indent)];
    case "tool-calls":
      return renderToolCalls(a, indent);
    case "judge":
      return [renderJudgeAssertion(a, indent)];
    default: {
      const exhaustive: never = a;
      throw new Error(`unknown assertion kind: ${JSON.stringify(exhaustive)}`);
    }
  }
};

const renderOutputMatchObject = (a: OutputMatchObjectAssertion, indent: string): string => {
  return `${indent}expect(result.output).toMatchObject(${JSON.stringify(a.value)});`;
};

const renderToolCalls = (a: ToolCallsAssertion, indent: string): string[] => {
  const exp: ToolCallExpectation = a.expected;
  switch (exp.type) {
    case "names-equal":
      return [`${indent}expect(toolNames).toEqual(${JSON.stringify(exp.names)});`];
    case "names-include":
      return [
        `${indent}expect(toolNames).toEqual(expect.arrayContaining(${JSON.stringify(exp.names)}));`,
      ];
    case "names-exclude":
      return exp.names.map(
        (n) => `${indent}expect(toolNames).not.toContain(${JSON.stringify(n)});`,
      );
    default: {
      const exhaustive: never = exp;
      throw new Error(`unknown tool-calls expectation: ${JSON.stringify(exhaustive)}`);
    }
  }
};

const renderJudgeAssertion = (a: JudgeAssertion, indent: string): string => {
  return `${indent}await expect(result).toSatisfyJudge(${a.judgeName});`;
};

// ── renderJudgesFile (suite-wide _judges.ts) ───────────────────────────────

/**
 * Render the suite-wide `evals/_judges.ts` exporting every
 * canonical judge in the skill. Sorted by name for stable output.
 */
export const renderJudgesFile = (judges: JudgePlan[]): string => {
  for (const j of judges) validateJudgeShape(j);
  if (judges.length === 0) {
    return `${JUDGES_TS_BANNER}// No judges declared.
export {};
`;
  }

  // oxlint-disable-next-line unicorn/no-array-sort
  const sorted = [...judges].sort((a, b) => a.name.localeCompare(b.name));
  const exports = sorted
    .map(
      (j) =>
        `export const ${j.name} = criterionJudge(${JSON.stringify(j.name)}, ${JSON.stringify(j.criterion)});`,
    )
    .join("\n\n");

  return `${JUDGES_TS_BANNER}import { criterionJudge } from "@sentry/skillet/evals";

${exports}
`;
};

// ── Helpers ────────────────────────────────────────────────────────────────

/**
 * Format a number for use in generated TS. Adds `_000` separators
 * for large round numbers so the file reads naturally
 * (`180_000` instead of `180000`).
 */
const formatNumber = (n: number): string => {
  if (n % 1000 === 0 && n >= 1000) {
    const k = n / 1000;
    return `${k}_000`;
  }
  return String(n);
};
