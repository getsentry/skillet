import type { AnyModel } from "../../agent/provider.js";
import type { SkillSpec } from "../../spec/index.js";
import { generateEvalYamlWithRetry } from "../eval-gen.js";
import { buildEvalGenPrompt } from "../prompts/eval-gen.js";

/**
 * The derived banner placed at the top of each generated eval YAML.
 * Mirrors the spec.yaml banner: humans see that the file is managed
 * by skillet and that hand edits will be overwritten.
 *
 * The eval parser ignores comment lines, so the banner has no effect
 * on parsing.
 */
export const EVAL_YAML_BANNER = `# ──────────────────────────────────────────────────────────
# Derived from spec.yaml. Do NOT edit this file by hand.
# Re-run via \`skillet spec refine\`, \`skillet add-eval\`, or
# \`skillet improve\` to regenerate. Hand edits will be lost.
# ──────────────────────────────────────────────────────────
`;

/**
 * Run the eval-gen phase: SkillSpec.behaviors + must_not → eval YAML.
 *
 * Wraps the existing lint+retry loop (`generateEvalYamlWithRetry`) so
 * load-bearing lint warnings still get fed back to the model. The
 * input format is structured JSON instead of SKILL.md prose; the
 * prompt enforces 1:1 mapping (one case per spec entry, named
 * `<id>__<slug>`, tagged `tests_behavior: <id>`).
 *
 * Returns the YAML string ready to be written to disk; the caller
 * prepends the derived-file banner via `EVAL_YAML_BANNER` if desired.
 */
export const runEvalGen = async (
  model: AnyModel,
  spec: SkillSpec,
  opts: { logProgress?: ((msg: string) => void) | undefined } = {},
): Promise<string> => {
  const input = {
    behaviors: spec.behaviors.map((b) => ({
      id: b.id,
      statement: b.statement,
      rationale: b.rationale,
      eval: b.eval,
    })),
    must_not: spec.must_not.map((m) => ({
      id: m.id,
      statement: m.statement,
      rationale: m.rationale,
      leakage_risk: m.leakage_risk,
      eval: m.eval,
    })),
  };

  const userContent = `Generate eval cases for the following spec entries:\n\n\`\`\`json\n${JSON.stringify(input, null, 2)}\n\`\`\``;

  return generateEvalYamlWithRetry({
    model,
    systemPrompt: buildEvalGenPrompt(),
    initialUserContent: userContent,
    ...(opts.logProgress != null ? { logProgress: opts.logProgress } : {}),
  });
};
